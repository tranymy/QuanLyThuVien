    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblDocGia.getModel();
        model.setRowCount(0);
        try {
            List<DocGia> list = DGdao.selectAll();
            for (DocGia dg : list) {
                Object[] row = {
                    dg.getMaDG(),
                    dg.getTenDG(),
                    dg.getSDT(),
                    dg.getLOP(),
                    dg.getNGAYSINH(),
                    dg.isGIOITINH() ? "Nam": "Nữ",
                    dg.getMANV()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu nhân viên!");
            e.printStackTrace();
        }
    }
    
    
